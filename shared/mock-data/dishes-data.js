// Helper function to get dish by ID
export const getDishById = (id) => {
  return dishes.find(dish => dish.id === id);
};

// Helper function to get dishes by category
export const getDishesByCategory = (category) => {
  return dishes.filter(dish => dish.category === category);
};

// Helper function to get top-rated dishes
export const getTopRatedDishes = (limit = 5) => {
  return [...dishes]
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit);
};

// Helper function to get most wasted dishes
export const getMostWastedDishes = (limit = 5) => {
  return [...dishes]
    .sort((a, b) => b.waste_rate - a.waste_rate)
    .slice(0, limit);
};

// Helper function to get least wasted dishes
export const getLeastWastedDishes = (limit = 5) => {
  return [...dishes]
    .sort((a, b) => a.waste_rate - b.waste_rate)
    .slice(0, limit);
};

export default dishes;
